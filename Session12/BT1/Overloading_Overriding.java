package Session12.BT1;

public class Overloading_Overriding {
    // Tiêu chí	Overloading (Nạp chồng phương thức)	Overriding (Ghi đè phương thức)
    //Khái niệm	Khi một lớp có nhiều phương thức cùng tên nhưng khác tham số (số lượng, kiểu dữ liệu, hoặc thứ tự tham số).	Khi một lớp con định nghĩa lại phương thức của lớp cha với cùng tên, cùng tham số và kiểu trả về.
    //Thời điểm quyết định	Được xác định tại thời điểm biên dịch (Compile-time).	Được xác định tại thời điểm chạy (Runtime).
    //Sự liên quan đến đối tượng	Xảy ra trong cùng một lớp.	Xảy ra giữa lớp cha và lớp con (kế thừa).
    //Số lượng tham số	Có thể khác nhau (có thể thay đổi số lượng, kiểu dữ liệu, thứ tự tham số).	Phải giữ nguyên số lượng và kiểu tham số của phương thức lớp cha.
    //Mục đích sử dụng	Cung cấp nhiều cách gọi phương thức khác nhau trong cùng một lớp với mục đích khác nhau.	Cho phép lớp con điều chỉnh hoặc thay đổi hành vi của phương thức lớp cha.
}
